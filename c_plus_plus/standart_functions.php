        <p>- математичні; </p>
        <p>- функції роботи з часом та датою; </p>
        <p>- функції роботи  з стрічками; </p>
        <p>- функції обробки помилок;</p>
        <p>- та інші. </p>
        <p>Для  використовування  всіх  математичних  функцій  в  програму  необхідно 
включити заголовок &lt;math.h>. </p>
        <p>Ці підпрограми виконують математичні обчислення і перетворення. </p>
        <p>abs   (x)- обчислює  абсолютне значення цілого числа; </p>
        <p>acos (x) - обчислює  арккосинус;</p>
        <p>asin (x) - обчислює  арксинус; </p>
        <p>atan (x) - обчислює  арктангенс; </p>
        <p>complex   (x)  - створює комплексні числа; </p>
        <p>cos  (x) - обчислює  косинус; </p>
        <p>cosh (x) - обчислює  гіперболічний косинус; </p>
        <p>exp  (x) - функція експоненти, повертає значення в cтепені x; </p>
        <p>fabs (x) - обчислює  абсолютне значення числа з плаваючою    крапкою; </p>
        <p>ceil (x) - округлення; </p>
        <p>pow  (x,y) - обчислює x в степені y; </p>
