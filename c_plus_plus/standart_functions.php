        <h2>Функції  дати та часу </h2>
        <p>Бібліотека  С++  містить  функції  для  роботи  з  часом  та  датою. Прототипи функцій знаходяться у файлі time.h.</p>
        <p>Функції роботи з часом в  С++  представляють  час    двома  способами.    Перший  –  це  кількість секунд  з 1 січня 1970 року. Для представлення часу раніше використовуються від’ємні значення. Значення представляються типом long int. В файлі time.h за допомогою  ключового    typedef  визначені  символьні  імена  time_t  i  clock_t  з значенням  long.  Другий  спосіб  –  представлення  часу,    розбитого  на  частини: рік,  місяць,  день  і  т.д.  Це  представлення  використовує  структуру  tm,  яка визначена  у файлі time.h. </p>
        <p>Одержання текучого часу </p>
        <p>Для  отримання  текучого  часу    з  внутрішнього  таймера    ПК  використовується функція time(). ЇЇ прототип має вигляд:</p>
<pre><code class="cpp"> time_t time (time_t *timeptr);

        Наприклад,

        time_t n;

        n=time(0);

        Змінна n буде містити текучий час. </code></pre>
        <p>Відображення часу на екрані </p>
        <p>Для перетворення  часу в форматовані стрічки використовуються функції 
ctime() та asctime(). Обидві функції  повертають час у вигляді стрічки . ctime() 
приймає час у вигляді аргумента типу time_t , а asctime() приймає час у вигляді 
аргумента типу tm. </p>
        <p>Вони мають наступний прототип: </p>
<pre><code class="cpp"> char *ctime(time_t * ptr );

        char asctime (struct tm * ptr ); </code></pre>
        <p>Визначення різниці часу </p>
        <p>Різниця  між  двома  моментами  часу  в  секундах  обчислюється 
за допомогою функції difftime (). Прототип функції: </p>
<pre><code class="cpp"> double difftime (time_t k, time_t p);</code></pre>
        <p>Функція  обчислює  різницю  k  i  p  та  повертає  кількість 
секунд між двома моментами часу. </p>
        <h2>Функції випадкового числа </h2>
        <p>Розглянемо оператор. </p>
<pre><code class="cpp"> А=rand();</code></pre>
        <p>Функція  rand  генерує  ціле  значення  в  діапазоні  від  0  до  RAND_MAX 
(символьна  константа,  яка  визначає  в  файлі  заголовку  &lt;stdlib.h>  ).  Стандарт 
ANSI оголошує, що значення RAND_MAX повинно дорівнювати 32767, тобто 
потрібно певні задачі. </p>
        <p>Функція rand генерує насправді псевдовипадкові числа. При виклику rand 
генерується  послідовність  чисел,  які  виглядають  випадковими.  Однак,  ця 
послідовність  повторюється  при  кожному  новому  виконанні  програми.  Дану 
програму можна модифікувати для генерації різних послідовностей випадкових 
чисел для кожного виконання. Це називається рандомізацією і забезпечується 
функцією  стандартної  бібліотеки  srand.  Функція  srand  отримує  для  аргумента 
ціле без знака (тип unsigned), яке дозволяє отримати різні послідовності чисел 
при кожному виконанні програми. </p>
        <p>Для  читання  даних  типу  unsigned  функцією  scanf  використовується 
специфікація  перетворення  %u.  Прототип  функції  для  srand  знаходиться  в 
&lt;stdlib.h> </p>
