                        <p>Масив – це група комірок пам`яті , які логічно зв`язані між собою (мають 
                        одне ім`я та однаковий тип). </p>
                        <p>Масиви бувають одновимірні та двовимірні. </p>
                        <p>Одновимірний  масив  (лінійна  таблиця)  –  це  масив  в  якому  елементи 
                        розміщуються в одному рядку або одному стовпці.  </p>
                        <p>Двовимірний  масив  (прямокутна  таблиця)  це  масив,  в  якому  елементи 
                        розміщуються як по рядках так і по стовпцях. </p>
                        <p>Масиви характеризуються: </p>
                        <ul>
                                <li> - іменем; </li>
                                <li> - розміром; </li>
                                <li> - розмірністю. </li>
                        </ul>
                        <p>Ім'я  масиву  –  А,  ВС,  С1,  MAS(буква,  буквосполучення  ,буква  і  цифра, 
                        слово) </p>
                        <p>Ім'я масиву не повинно співпадати ні з однією простою змінною, яка   
                        використовується в цьому ж алгоритмі ( програмі).</p>
                        <p>Розмірність – одновимірний або двовимірний.</p> 
                        <p>одновимірний - лінійна таблиця; </p> 
                        <p>двовимірний - прямокутна таблиця; </p>
                        <p>Розмір масиву-кількість елементів в масиві.</p>
                        <p>Кількість  елементів  одновимірного  масиву  рівна  різниці  верхньої  і 
                        нижньої межі +1 </p>
                        <p>Кількість елементів двовимірного масиву рівна добутку кількості рядків і 
                        стовпців.</p> 
                        <p>Для звернення до елемента масиву потрібно вказати ім`я масиву та номер 
                        позиції цього елемента в масиві .</p> 
                        <p>На  кожен  елемент  масиву  можна  посилатися,  вказавши  ім`я  масиву  та 
                        його  номер  позиції  в  квадратних  дужках  ([  ]).  Перший  елемент  в  будь-якому 
                        масиві має нульовий порядковий номер. Номер позиції елемента в квадратних 
                        дужках називають індексом. Індекс може бути цілим числом або цілочисельним 
                        виразом.</p> 
                        <p>Зауваження! Важливо пам`ятати різницю між сьомим елементом масиву і 
                        елементом  масиву  номер  сім.  Оскільки  індекс  масиву  починаються  з  нуля,  а 
                        сьомий елемент масиву має індекс 6.</p> 
                        <p>Квадратні дужки мають такий же пріоритет, як і  круглі дужки. </p>
                        <h2>Оголошення та ініціалізація масивів</h2>  
                        <p>Масиви  займають  визначене  місце  в  пам`яті  комп‟ютера.  Програмістом 
                        встановлюється  тип  кожного  елемента  і  число  елементів,  які  потрібні  для 
                        кожного масиву, щоб комп‟ютер міг зарезервувати відповідний об`єм пам`яті.</p>  
                        <p>Щоб  вказати  комп‟ютеру  зарезервувати  10  елементів для  цілочисельного 
                        масиву а потрібно написати:</p>  
    <pre><code class="cpp">
    int a[10];
    </code></pre>  
                        <p>За допомогою одного оголошення можна зарезервувати пам`ять декількох 
                        масивів. </p> 
                        <p>Наприклад:</p>  
    <pre><code class="cpp">
    int b[100], y[25];

    for (i=0; i<=9; i++)

    a [i]=0; /*ініціалізація масиву*/
    </code></pre>
                        <p>Якщо  ініціалізуючих  значень  менше  ніж  елементів  масиву,  то  елементи, 
                які залишилися автоматично ініціалізуються нулями. </p>
                        <p>Наприклад: int a[10]={0};</p>
                        <p>Ініціалізує нулями перший елемент  і автоматично ініцалізує нулями решту 
                дев`ять елементів . </p>
                        <p>Зауваження! Масиви не ініціалізуються нулями автоматично . Програміст 
                повинен  ініціалізувати  хоча  б  перший  елемент,  щоб  всі  інші  ініціалізувалися 
                нулями. Метод ініціалізації нулями реалізується під час компіляції. </p>
                        <p>Зауваження!  Якщо  розмір  масиву  не  вказується  в  його  оголошення  із 
                списком ініціалізуючих значень, то число елементів дорівнює числу елементів в 
                списку ініціалізації. </p>
                        <p>Наприклад , оголошенням </p>
    <pre><code class="cpp">
    int a[ ]={1,2,3,4,5};
    </code></pre>
