                        <h2>Багатовимірні масиви </h2>
                        <p>Масиви  в  С  можуть  мати  декілька  індексів.  Багатовимірні  масиви  часто 
                використовуються, для представлення таблиць, які складаються із значень, які 
                впорядковані  по  рядках  та  стовпцях.  Для  опрацювання  таких  масивів 
                використовуються два індекси, один представляє рядки, другий стовпці . </p>
                        <p>Масиви, які мають два індекси, називаються двовимірними масивами.</p>
                        <p>Кожні елементи двовимірного масиву а ідентифікується  як а [i] [j], де і – 
                рядок, j – стовпець.</p>
                        <p>Двовимірний  масив,  подібно  одновимірному,  може  бути  ініціалізований 
                при його оголошені. </p>
                        <p>Наприклад,  двовимірний  масив  b[2]  [2]  може  бути  оголошений  і 
                ініціалізований;</p>
    <pre><code class="cpp">
    int b [2] [2]={{1,2},{3,4}};
    </code></pre>
                        <p>Значення групуються  в фігурних дужках по стрічках.</p>
                        <p>Зауваження! Якщо для даної стрічки недостатньо  ініціалізуючих значень, 
                то елементи, які залишились ініціалізуються  нулями.</p>
                        <p>Наприклад, оголошення </p>
    <pre><code class="cpp">
    int b[2] [2]={{1},{4,3 }};
    </code></pre>
                        <p>ініціалізує  b[0] [0]=1 , b[0] [1]=0, b[1] [0]=3, b[1] [1]=4 </p>
                        <p>Якщо  двовимірний  масив  ініціалізується  одним  рядком  то  спочатку 
                заповнюється перший рядок, а ті елементи які залишились заповнюють другий 
                рядок і т.д.</p>
                        <p>Всі  елементи  масивів  зберігаються  в  пам`яті  комп‟ютера  послідовно, 
                незалежно від кількості індексів.</p>
                        <p>В двовимірному масиві за першим рядком в пам`яті іде другий і т. д.</p>
                        <p>Вказання  значень  індексів  при  оголошенні  параметрів  функції  дає 
                можливість  компілятору  повідомити    їй  про  спосіб  відшукання  елементів  в 
                масиві.  Для  визначення  місця  знаходження  елемента  в  конкретному  рядку 
                компілятор  повинен    знати,  скільки  елементів  знаходиться  в  кожній  стрічці, 
                щоб при зверненні до цього масиву він міг пропустити  відповідне число блоків 
                пам`яті. </p> 
